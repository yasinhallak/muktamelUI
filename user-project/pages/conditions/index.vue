      </p>
      <p dir="RTL">
        1- نحن لا نتحمل أي مسؤولية تجاه روابط المواقع ومحتوياتها، ولا نمتلك أي
        تأثير على تصميم أو تعديل المواقع الخاصة بهذه الروابط ولا على المحتوى
        الموجود عليها أو على أي من المنتجات أو الخدمات المقدمة من قبلهم أو من
        خلالهم.
      </p>
      <p dir="RTL">
        2- وجود مثل هذه الروابط في موقعنا لا تمثل ولا يعتبر بأي حال من الأحوال
        توصية بها و/أو موافقة منا عليها، واستخدامك لأي من المنتجات والخدمات المقدمة
        من قبلها أو من خلالهم سوف يكون على مسؤوليتك الخاصة، ولا نتحمل أي مسؤولية عن
        أي خسارة أو ضرر من أي نوع ناتج عن استخدامك لمثل هذه المواقع أو اعتمادك لها،
        وتقرون إخلاء طرفنا من أي مطالبات أو منازعات من أي نوع تنشأ بأي طريقة وتكون
        متعلقة مع أطراف خارجية.
      </p>
      <p dir="RTL">
        3- إذا كانت الروابط التشعبية الموجودة تشير إلى أطرافٍ ثالثة فنحن لا نحدد أو
        نعلم أو نخمن ما قد يوجد عليها، ولا نتحمل أي مسؤولية تحديداً تجاه دقة أو
        اكتمال أو شرعية أو موضوعية أو حداثة المحتوى عليها، ولا نقوم بمراقبتها أو
        مراجعتها أو المصادقة عليها.
      </p>
