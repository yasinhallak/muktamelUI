        ب- أي فقدان لكلمة المرور أو الحساب لأي أسباب كانت، أو عدم قدرتكم على
        استخدام موقعنا.
        <br/>
        ت- أي خسارة مباشرة أو تبعية أو خاصة أو جزائية، أو ضرر، أو تكاليف ونفقات لأي
        نوع من أنواع الخسارة مهما كان نوعها مادية أو معنوية، نتجت من اعتمادكم على
        الموقع أو على أي محتوى أو خدمات أو معلومات تم عرضها على موقعنا أو أحد
        أجزائه أو مكوناته، بما في ذلك، على سبيل المثال لا الحصر، خسارة الأرباح أو
        الأعمال أو السمعة أو الشهرة أو تراجعها أو ضياع الفرص أو فقد البيانات أو
        تلفها أو فسادها.
