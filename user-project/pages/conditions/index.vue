        ث- أي مسؤولية عن خسارة تتعلق بالتزاماتنا المباشرة أو غير المباشرة الناتجة
        عن انقطاع الخدمات التي تقدمها أطراف أخرى أو تلك التي تنتج عن محاولة
        المستخدم أو العميل ربط موقعنا بأي تطبيق أو موقع آخر، أو أي حالات غير مصرح
        بها أو خارجة عن إرادتنا.
        <br/>
        ج- أي خسارة أو ضرر أو مسؤولية ناتجة من أي فيروس أو هجوم لقطع الخدمة أو مواد
        أخرى ضارة تقنيًّا تصيب حواسيبكم أو برامجكم الحاسوبية أو البيانات أو
        الممتلكات الأخرى الخاصة بكم نتيجة استخدامكم موقعنا أو تحميلكم أي محتوى عليه
        أو على أي موقع مرتبط به، ونوصيكم بحفظ نسخة احتياطية من أي بيانات تخصكم على
        موقعنا تحسبًا لحدوث مشاكل غير متوقعة في الموقع.
      </p>
      <p dir="RTL">
        9- توافقون على عدم السماح لكم باستخدام الموقع الالكتروني لأي أغراض تجارية
        أو أعمال تجارية، ما لم يتم التصريح لكم بذلك منا.
      </p>
      <p dir="RTL">
        <strong><u>الملكية الفكرية</u></strong>
      </p>
      <p dir="RTL">
        1- ما لم يذكر خلاف ذلك صراحةً، فنحن نحتفظ بكافة حقوق الملكية الفكرية للموقع
        الإلكتروني وجميع خدماته وجميع محتوياته، وجميع الشركات والمواقع التابعة لنا
        أو المحتوى الذي رخصت لنا أطراف ثالثة استخدامه بما في ذلك -على سبيل المثال
        لا الحصر- العلامات التجارية والمظهر التجاري والتصميمات والنصوص والرسومات
        وطرق اختيارها وكيفية ترتيبها، وجميع محتويات الموقع تحمل حقوق التأليف والنشر
        وهي محمية بموجب القوانين المعمول بها.
      </p>
      <p dir="RTL">
        2- الأسماء والشعارات وجميع ما يتصل بها من منتجات وأسماء الخدمات، وتصميم
        العلامات والشعارات هي علامات تجارية أو علامات خدمة لنا أو للمرخصين لنا، ولا
        يجوز منح أي ترخيص للعلامة التجارية أو علامة الخدمة فيما يتعلق بالمواد
        الواردة في هذا الموقع.
      </p>
      <p dir="RTL">
        3- نحن نحتفظ (بما في ذلك عملاؤنا والمرخصون لنا) بجميع حقوق الملكية الفكرية
        في كافة النصوص والبرامج، والمنتجات والعمليات، والتكنولوجيا، والمحتوى وغيرها
        من المواد التي تظهر على هذا الموقع أو الأجزاء التابعة له. ولا يجوز لأي شخص
        أن يعطي إذنًا بالوصول إلى هذا الموقع لأي شخص أو الحصول على أي ترخيص تحت أي
        من حقوق الملكية الفكرية لنا أو لأي طرف ثالث.
      </p>
      <p dir="RTL">
        4- يحظر أي استخدام لهذا الموقع أو محتوياته، بما في ذلك النسخ أو التخزين،
        كلياً أو جزئياً، خلاف استخدامك الشخصي وغير التجاري، دون إذن منا. ولا يجوز
        لكم تعديل أو توزيع أو إعادة نشر أي شيء على هذا الموقع لأي غرض من الأغراض.
      </p>
      <p dir="RTL">
        5- وصولكم إلى هذا الموقع أو قبول تسجيلكم فيه أو ظهوره لكم من خلال محركات
        البحث لا يخولكم استخدام أي اسم، أو شعار أو علامة تجارية أو أي محتوى آخر
        متاح في الموقع إلا إذا كنتم تملكونها رسميًّا.
      </p>
      <p dir="RTL">
        6- باستخدامكم للموقع فإنكم تمنحونا رخصة عالمية لا رجعة فيها، وبلا أي مقابل
        أو اعتراض مهما كان نوعه بأحقيتنا بامتلاك وتسويق ونسخ وتعديل وترخيص
        الاستخدام للآخرين لأي محتوى تستخرجونه أو تقوموا بإضافته أو استضافته في
        الموقع
      </p>
      <p dir="RTL">
        7- لا يجوز تأويل أي محتوى وارد في الموقع على أنه مُنح ضمنياً أو غير ذلك أو
        أنه ترخيص أو حق لاستخدام دون إذن خطي.
      </p>
      <p dir="RTL">
        8- نحن نأخذ عمليات انتهاك حقوق الملكية الفكرية وعمليات الاحتيال الفعلية
        والمُبلّغ عنها على محمل الجد ولنا الحق باتخاذ كافة الإجراءات القانونية
        اللازمة وفق الأنظمة النافذة، ولا يحق للمستخدمين تحميلنا أي مسؤولية فيما
        يتعلق بقضايا مماثلة صادرة من أحد عملاء أو مستخدمي الموقع، بل على كافة
        المستخدمين إبلاغنا عن أي انتهاكات أو عمليات مماثلة على الفور، وسنقوم
        بالإبلاغ عنها للجهات المختصة.
      </p>
      <p dir="RTL">
        <strong><u>توفر الخدمة:</u></strong>
      </p>
