        أو تصحيح الأخطاء مما يضطرنا لحجب الموقع وخدماته (كليًّا أو جزئيًّا) دون
        إشعار مسبق.
      </p>
      <p dir="RTL">
        2- رغم سعينا الحثيث إلى تقليل فترة عدم توفر الموقع إلى أقل حد ممكن، إلا
        أننا لا نتحمل بأي حال من الأحوال ومهما كانت الأضرار أي مسؤولية ناتجة عن
        التوقف أو فقدان الخدمة.
      </p>
      <p dir="RTL">
        3- تقرون إقرارا مطلقا بحقنا الكامل في تغيير أو تعليق أو قطع أو إيقاف الموقع
        وخدماته (كليًّا أو جزئيًّا) في جميع الأوقات بما في ذلك حق الدخول الخاص بك
        دون أدنى اعتراض منكم.
      </p>
      <p dir="RTL">
        <strong><u>الروابط والأطراف الخارجية:</u></strong>
      </p>
      <p dir="RTL">
