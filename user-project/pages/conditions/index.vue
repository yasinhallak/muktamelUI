        ذ- استخدام أي خدمة بشكل غير قانوني أو غير أخلاقي أو مؤذي لموقع مكتمل أو
        عملائه أو مستخدميه أو ملاكه أو الغير أو تنتهك السياسات والإشعارات الأخرى
        على الموقع.
        <br/>
        ر- استخدام الموقع أو خدماته لأهداف تتخطى الاستخدام الشخصي أو تزيد عن الحدود
        الممنوحة للعملاء المسجلين في مكتمل.
