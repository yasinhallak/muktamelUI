        المستخدمين الآخرين ومدى انتفاعهم من الموقع أو أي من خدماته.
        <br/>
        ح- إنشاء حسابات بخدماتنا من خلال وسائل غير مصرح بها أو وسائل تلقائية.
        <br/>
        خ- إرسال أو كتابة أي رسائل أو تعليقات مسيئة أو غير لائقة أو مضللة إلى
        الموقع أو أحد موظفينا أو عملائنا أو مستخدمينا، أو انتهاج سلوك عدواني أو
        بذيء ضدهم.
        <br/>
        د- نشر أي مادة غير مقبولة تشجع أو تدعو إلى انتهاك أي من القوانين النافذة أو
        توجيهات الجهات والهيئات المسؤولة.
