        ز- حذف أو تعديل أو التلاعب أو تحوير أو إعادة استعمال أي إشعارات حقوق النشر
        لأي مادة من المواد التي تظهر على الموقع.
        <br/>
        س- تجميع معلومات حول أو عن مستخدمينا بأي وسيلة غير مسموح بها أو غير مصرح
        بها.
