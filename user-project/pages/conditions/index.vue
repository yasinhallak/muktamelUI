        مخالفة من مخالفات المحتوى المرفوض الموضحة في شروط الاستخدام.
      </p>
      <p dir="RTL">
        8- تقرون بأننا لن نتحمل أي مسؤولية تجاهكم عن أية خسارة أو ضرر، ناشئ من أو
        متعلق بالآتي:
